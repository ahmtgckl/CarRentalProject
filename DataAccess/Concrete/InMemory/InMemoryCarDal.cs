    public class InMemoryCarDal : ICarDal
    {
        List<Car> _cars;
        public InMemoryCarDal()
        {
            _cars = new List<Car>
            {
                new Car{CarId=1,BrandId=3,ColorId=2,ModelYear=2018,DailyPrice=180,Description="PASSAT"},
                new Car{CarId=2,BrandId=6,ColorId=2,ModelYear=2015,DailyPrice=150,Description="YARİS"},
